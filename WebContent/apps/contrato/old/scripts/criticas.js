function critica_inclusao_contrato(f)
{
  var msg = "";

  if (f.numContrato.value.replace(/^\s*|\s*$/g,"")=="")
  {
     msg = msg + "- O campo NÚMERO DO CONTRATO é de preenchimento obrigatório.\n";
  }
  
  if (f.anoContrato.value.replace(/^\s*|\s*$/g,"")=="")
  {
     msg = msg + "- O campo ANO DO CONTRATO é de preenchimento obrigatório.\n";
  }
  
  if (!EhData(f.vigenciaIni.value))
  {
     msg = msg + "- A VIGÊNCIA INICIAL não é válida.\n";
  }
  
  if (!EhData(f.vigenciaFim.value))
  {
     msg = msg + "- A VIGÊNCIA FINAL não é válida.\n";
  }

  if (f.descricao.value.replace(/^\s*|\s*$/g,"")=="")
  {
     msg = msg + "- O campo DESCRIÇÃO DO CONTRATO é de preenchimento obrigatório.\n";
  }
  
  //verifica se o campo data da publicação é válido
  if (!EhData(f.dataPublicacao.value))
  {
     msg = msg + "- A DATA DE PUBLICAÇÃO não é válida.\n";
  }
  if (f.anexo.value.replace(/^\s*|\s*$/g,"")=="")
  {
     msg = msg + "- O ARQUIVO não foi selecionado.\n";
  }
  
  //Se não houver mensagens de erro o submit e acionado no form correspondente
  if (msg.replace(/^\s*|\s*$/g,"")=="")
  {  
	  f.submit();
	  startProgress();
  }
  else
  {
     alert("Ocorreram os seguintes erros:\n\n" + msg);
	 return false;
  }
  
}


