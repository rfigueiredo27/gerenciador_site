package br.jus.trerj.modelo;

public class Destaque {
	private String descricao;
	private String idConteudo;
	private String idArquivo;
	private String nomeArquivo;
	private String observacao;
	private String dataIni;
	private String dataFim;
	private int ordem;
	private int publicado;
	private int temAnexo;
	private String descricaoAnexo;

	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getIdConteudo() {
		return idConteudo;
	}
	public void setIdConteudo(String idConteudo) {
		this.idConteudo = idConteudo;
	}
	public String getIdArquivo() {
		return idArquivo;
	}
	public void setIdArquivo(String idArquivo) {
		this.idArquivo = idArquivo;
	}
	public String getNomeArquivo() {
		return nomeArquivo;
	}
	public void setNomeArquivo(String nomeArquivo) {
		this.nomeArquivo = nomeArquivo;
	}
	public String getObservacao() {
		return observacao;
	}
	public void setObservacao(String observacao) {
		this.observacao = observacao;
	}
	public String getDataIni() {
		return dataIni;
	}
	public void setDataIni(String dataIni) {
		this.dataIni = dataIni;
	}
	public String getDataFim() {
		return dataFim;
	}
	public void setDataFim(String dataFim) {
		this.dataFim = dataFim;
	}
	public int getOrdem() {
		return ordem;
	}
	public void setOrdem(int ordem) {
		this.ordem = ordem;
	}
	public int getPublicado() {
		return publicado;
	}
	public void setPublicado(int publicado) {
		this.publicado = publicado;
	}
	public int getTemAnexo() {
		return temAnexo;
	}
	public void setTemAnexo(int temAnexo) {
		this.temAnexo = temAnexo;
	}
	public String getDescricaoAnexo() {
		return descricaoAnexo;
	}
	public void setDescricaoAnexo(String descricaoAnexo) {
		this.descricaoAnexo = descricaoAnexo;
	}
	

}
