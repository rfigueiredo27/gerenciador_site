package br.jus.trerj.modelo;


public class Componente {
	private String id;
	private String classe;
	private String tipo;
	private String cargo;
	private String nome;
	private String mandatoInicial;
	private String mandatoFinal;
	private String bienio;
	private String obs;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMandatoInicial() {
		return mandatoInicial;
	}
	public void setMandatoInicial(String mandatoInicial) {
		this.mandatoInicial = mandatoInicial;
	}
	public String getMandatoFinal() {
		return mandatoFinal;
	}
	public void setMandatoFinal(String mandatoFinal) {
		this.mandatoFinal = mandatoFinal;
	}
	public String getBienio() {
		return bienio;
	}
	public void setBienio(String bienio) {
		this.bienio = bienio;
	}
	public String getObs() {
		return obs;
	}
	public void setObs(String obs) {
		this.obs = obs;
	}
	
	
	

}
